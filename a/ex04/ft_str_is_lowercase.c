int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	while (str[i] != '\0')
	{
		a = ((str[i] >= 'a') && (str[i] <= 'z'));
		if (!(a))
			return (0);
		i++;
	}
	return (1);
}
