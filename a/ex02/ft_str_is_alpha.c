int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	while (str[i] != '\0')
	{
		a = str[i] >= 'A' && str[i] <= 'Z';
		b = ((str[i] >= 'a') && (str[i] <= 'z'));
		if (!(a || b))
			return (0);
		i++;
	}
	return (1);
}
